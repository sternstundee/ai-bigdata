# 实例3：字符串切片（基于样本字符串：*重庆邮电大学-通信与信息工程学院-通信工程专业*）
# 定义样本字符串
s = "*重庆邮电大学-通信与信息工程学院-通信工程专业*"
print(f"原始字符串：{s}")

# 1. 输出字符串长度
print(f"\n1. 字符串长度：{len(s)}")

# 2. 切片输出“重庆邮电大学”（去掉开头*，索引1到7）
print(f"2. 切片结果（重庆邮电大学）：{s[1:7]}")

# 3. 切片拼接输出“重邮通信”（重：1:2，邮：3:4，通信：8:10）
part1 = s[1:2]  # 重
part2 = s[3:4]  # 邮
part3 = s[8:10] # 通信
print(f"3. 拼接结果（重邮通信）：{part1 + part2 + part3}")

# 4. 反向索引输出“通信”（第二个“通信”在“通信工程专业”，反向索引-7:-5）
print(f"4. 反向索引结果（通信）：{s[-7:-5]}")

# 5. split分割+逆序第二个子串（用“-”分割，取索引1的子串逆序）
split_list = s.split("-")  # 分割为["*重庆邮电大学", "通信与信息工程学院", "通信工程专业*"]
reverse_sub = split_list[1][::-1]  # 逆序第二个子串
print(f"5. 第二个子串逆序：{reverse_sub}")

# 6. 统计“通信”出现次数
count_comm = s.count("通信")
print(f"6. “通信”出现次数：{count_comm}")

# 7. replace替换“通信与信息工程”为“通信”
new_s = s.replace("通信与信息工程", "通信")
print(f"7. 替换后字符串：{new_s}")