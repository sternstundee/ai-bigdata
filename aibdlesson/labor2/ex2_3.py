try:
    # 输入利润（单位：万元，如123表示123万）
    profit = float(input("请输入员工为企业创造的利润（单位：万元）："))
    bonus = 0.0  # 初始化奖金

    # 阶梯计算奖金
    if profit <= 10:
        bonus = profit * 0.1  # 10万及以下：10%
    elif profit <= 20:
        bonus = 10 * 0.1 + (profit - 10) * 0.075  # 10-20万：10万部分10%，超出部分7.5%
    elif profit <= 40:
        bonus = 10 * 0.1 + 10 * 0.075 + (profit - 20) * 0.05  # 20-40万：超出20万部分5%
    elif profit <= 60:
        bonus = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + (profit - 40) * 0.03  # 40-60万：超出40万部分3%
    elif profit <= 100:
        bonus = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + (profit - 60) * 0.015  # 60-100万：1.5%
    else:
        bonus = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + 40 * 0.015 + (profit - 100) * 0.01  # 超100万：1%

    # 输出结果（要求输入123万时，输出16.75万）
    print(f"\n💰 该员工应获得的奖金为：{bonus:.2f} 万元")

except ValueError:
    print("❌ 输入错误！请输入有效的数字（如123、56.8）。")